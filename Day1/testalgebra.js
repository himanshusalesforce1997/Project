const addNumbers = (a, b) => a + b;
const subtractNumbers = (a, b) => a - b;

// Addition test cases
console.log(addNumbers(2, 3)); // 5
console.log(addNumbers(10, 20)); // 30
console.log(addNumbers(-1, 1)); // 0
console.log(addNumbers(0, 0)); // 0

// Subtraction test cases
console.log(subtractNumbers(5, 2)); // 3
console.log(subtractNumbers(20, 10)); // 10
console.log(subtractNumbers(-1, 1)); // -2
console.log(subtractNumbers(0, 0)); // 0
