const addNumbers = (a, b) => a + b;
const subtractNumbers = (a, b) => a - b;

const num1 = 10;
const num2 = 20;
const sumResult = addNumbers(num1, num2);
const subtractResult = subtractNumbers(num1, num2);

console.log(`Sum of ${num1} and ${num2} is ${sumResult}`);
console.log(`Difference of ${num1} and ${num2} is ${subtractResult}`);
